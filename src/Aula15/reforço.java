package Aula15;

// Nome da classe em PascalCase e sem caracteres especiais
public class reforço {

    // Adicionado o modificador 'public' para permitir a execução
    public static void main(String[] args) {

        // Loop de contagem regressiva
        for (int i = 3; i > 0; i--) {
            System.out.println(i);
        }

        System.out.println("Fim da contagem!");
    }
}